  y.^4*gx*gy+(-2).*x.^3.*(5.*y2+2.*z2)*gx*gy+(-3).*x.^4.*y.* ...
  gy^2+y.^5*(gx^2+(-4)*gy^2)+y.^3.*z2*(gx^2+(-1)*gy^2)+ ...
  x2.*(y.^3*(gx^2+(-7)*gy^2)+3.*y.*z2*(gx^2+(-1)*gy^2)));


Uz = (5./72).*z.*(x2+y2+z2).^(-5./2).*(3.*y.^4*gx^2+(-6).*x.^3.* ...
  y*gx*gy+(-6).*x.*y.^3.*gx.*gy+3.*x.^4.*gy^2+5.*y2.*z2*( ...
  gx^2+gy^2)+2.*z.^4.*(gx^2+gy^2)+x2.*(3.*y2+5.*z2)*( ...
  gx^2+gy^2));

%   z2.*(x2+y2+z2).^(-5./2)).*gx.*gy+((-5./12).*x.^4.*(x2+ ...
%   y2+z2).^(-5./2)+(-5./4).*x2.*y2.*(x2+y2+z2).^(-5./2)+( ...
%   -5./6).*y.^4.*(x2+y2+z2).^(-5./2)+(-5./12).*x2.*z2.*(x2+ ...
%   y2+z2).^(-5./2)+(5./12).*y2.*z2.*(x2+y2+z2).^(-5./2)).*gy2;
Udisc = [Ux;Uy;Uz];
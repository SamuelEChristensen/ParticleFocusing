y2 = y.^2;
z2 = z.^2;
Ux = (1./72).*(x2+y2+z2).^(-5./2).*((-5).*x.*(4.*x.^4+3.*y2.*( ...
  y2+z2)+x2.*(7*y2+z2))*gx^2+(-10).*y.*(3*x.^4+5.* ...
  x2.*y2+2.*y2.*(y2+z2))*gx*gy+5*x.*(x.^4+3.*y2.* ...
  z2+x2.*(y2+z2))*gy^2);

